    // 根据快递公司名称获取数据
    public static function getOne($param)
    {
        $return = false;
        if (!$param) {
            return $return;
        }
        $expressList = self::getExpressList();
        foreach ($expressList as $item) {
            if ($item['name'] == $param) {
                $return = $item;
                break;
            }
        }
        return $return;
    }

    /**
     * 获取数组中最后一个Id的值，用于追加数据
     * @return mixed
     */
    public static function highestId()
    {
        $express = self::getExpressList();
        $ids = [];
        foreach ($express as $item) {
            $ids[] = $item['id'];
        }
        rsort($ids);
        return reset($ids);
    }

