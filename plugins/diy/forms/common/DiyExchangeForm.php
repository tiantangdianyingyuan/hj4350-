namespace app\plugins\diy\forms\common;


use app\models\Goods;
use app\models\Model;

class DiyExchangeForm extends Model
{
    use TraitGoods;

    public function getGoodsIds($data)
    {
        $goodsIds = [];
        foreach ($data['list'] as $item) {
            $goodsIds[] = $item['id'];
        }

        return $goodsIds;
    }

    public function getGoodsById($goodsIds)
    {
        if (!$goodsIds) {
            return [];
        }

        $list = Goods::find()->where([
            'id' => $goodsIds,
            'status' => 1,
            'is_delete' => 0,
            'sign' => 'exchange',
        ])->andWhere(['id' => (new \app\plugins\exchange\Plugin())->getSignCondition([])])->with('goodsWarehouse')->all();

        return $this->getGoodsList($list);
    }

    public function extraGoods($arr, $goods)
    {
        $arr['page_url'] = '/plugins/exchange/goods/goods?goods_id=' . $goods->id;
        return $arr;
    }

    public function getNewGoods($data, $goods)
    {
        $newArr = [];
        foreach ($data['list'] as $item) {
            foreach ($goods as $gItem) {
                if ($item['id'] == $gItem['id']) {
                    $newArr[] = $gItem;
                    break;
                }
            }
        }

        $data['list'] = $newArr;

        return $data;
    }
}
